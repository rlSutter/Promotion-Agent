Write-Host "   http://localhost:5000" -ForegroundColor Gray
Write-Host ""
Write-Host "3. Check logs:" -ForegroundColor White
Write-Host "   docker-compose logs -f" -ForegroundColor Gray
Write-Host ""
Write-Host "For more information, see README.md" -ForegroundColor Yellow
Write-Host ""
